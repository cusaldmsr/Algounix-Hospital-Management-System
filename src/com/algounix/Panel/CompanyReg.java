    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {

            int row = jTable1.getSelectedRow();

            String id = jTextField3.getText();
            String company = jTextField1.getText();
            String hotline = jTextField2.getText();

            if (row == -1) {
                JOptionPane.showMessageDialog(this, "Pleace select a row", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (company.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Company Name");
            } else if (hotline.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Hotline Number");
            } else if (!hotline.matches("^07[01245678]{1}[0-9]{7}$")) {
                JOptionPane.showMessageDialog(this, "Invalid Hotline Number", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {

                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `company` WHERE  `company` = '" + company + "' OR `hotline` = '" + hotline + "' ");

                boolean canUpdate = false;

                if (resultSet.next()) {

                    if (!resultSet.getString("id").equals(id)) {
                        JOptionPane.showMessageDialog(this, "This Company or Hotline Number already registered", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {
                        canUpdate = true;
                    }

                } else {

                    canUpdate = true;

                }

                if (canUpdate) {

                    MySQL.executeIUD("UPDATE `company` SET `company` = '" + company + "' , `hotline` = '" + hotline + "'"
                            + "WHERE `id` = '" + id + "' ");

                    addCompany();
                    reset();
                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

