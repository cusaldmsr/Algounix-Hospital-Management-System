    private void getID() {
        try {
            ResultSet rs = MySQL.executeSearch("SELECT `id` FROM `company` ORDER BY `id` DESC LIMIT 1");

            if (rs.next()) {
                int currentID = rs.getInt("id");
                //System.out.println(currentID);

                String newID = String.valueOf(currentID + 1);
                jTextField3.setText(newID);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void addCompany() {

        try {

            ResultSet resultSet = MySQL.executeSearch("SeLECT * FROM `company`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("id"));
                vector.add(resultSet.getString("company"));
                vector.add(resultSet.getString("hotline"));

                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

        //generatecompanyid();
    }

