    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {

            String id = jTextField3.getText();
            String company = jTextField1.getText();
            String hotline = jTextField2.getText();

            if (id.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter id");
            } else if (company.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Company Name");
            } else if (hotline.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Hotline Number");
            } else if (!hotline.matches("^07[01245678]{1}[0-9]{7}$")) {
                JOptionPane.showMessageDialog(this, "Invalid Hotline Number", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `company` WHERE `id` = '" + id + "' OR `company` = '" + company + "' OR `hotline` = '" + hotline + "' ");

                if (resultSet.next()) {

                    JOptionPane.showMessageDialog(this, "This Company already registered", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

                    MySQL.executeIUD("INSERT INTO `company` (`id`,`company`,`hotline`)"
                            + "VALUES('" + id + "','" + company + "' , '" + hotline + "')");

                    addCompany();
                    reset();
                }

                getID();

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

