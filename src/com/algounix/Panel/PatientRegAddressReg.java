    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
         String pid = jTextField4.getText();
        String aId = jTextField9.getText();
        String line1 = jTextField10.getText();
        String line2 = jTextField11.getText();
       
        String city = String.valueOf(jComboBox3.getSelectedItem());
        String district = String.valueOf(jComboBox4.getSelectedItem());
        
        if (line1.isEmpty()){
           JOptionPane.showMessageDialog(this, "Please Enter Address Line 01", "Warning" , JOptionPane.WARNING_MESSAGE);
        }else if (line2.isEmpty()){
           JOptionPane.showMessageDialog(this, "Please Enter Address Line 02 ", "Warning" , JOptionPane.WARNING_MESSAGE);
        }else if (city.equals("Select")) {
           JOptionPane.showMessageDialog(this, "Please Select City ", "Warning" , JOptionPane.WARNING_MESSAGE);
        }else if (district.equals("Select")){
            JOptionPane.showMessageDialog(this, "Please Select District ", "Warning" , JOptionPane.WARNING_MESSAGE);
        
        }else{
        
            try {
                
              ResultSet resultSet =   MySQL.executeSearch(" SELECT * FROM `patient_address` WHERE `patient_id` = '"+pid+"' OR `id` = '"+aId+"' ");
                
                if (resultSet.next()){
                    
                     JOptionPane.showMessageDialog(this, "The Address Of This Patient Has Been Registered", "Warning" , JOptionPane.WARNING_MESSAGE);
                
                }else {
                    
                    MySQL.executeIUD("INSERT INTO `patient_address` (`id` , `line1` , `line2` ,`city_id` , `district_id` , `patient_id`) VALUES "
                                                      +"('"+aId+"' , '"+line1+"' , '"+line2+"' , '"+cityMap.get(city)+"' , '"+districMap.get(district)+"' , '"+pid+"') ");
                    
                    JOptionPane.showMessageDialog(this, "This Patient Address Registered Successfully", "Infomation" , JOptionPane.INFORMATION_MESSAGE);
                    reset();
                }
                
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jTextField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyReleased
        // TODO add your handling code here:
        
         try {
            
            String nic = jTextField2.getText();
            
            if(nic.isEmpty()){
              jLabel2.setText("Patient Name");
              jTextField4.setText("");
            
            }else if(!nic.matches("^(([5,6,7,8,9]{1})([0-9]{1})([0,1,2,3,5,6,7,8]{1})([0-9]{6})([v|V|x|X]))|(([1,2]{1})([0,9]{1})([0-9]{2})([0,1,2,3,5,6,7,8]{1})([0-9]{7}))")){
                       jTextField2.setForeground(Color.red);
                       
                       jLabel2.setText("No Patient");
                        jTextField4.setText("");
                        reset();
                        
            }else if(nic.matches("^(([5,6,7,8,9]{1})([0-9]{1})([0,1,2,3,5,6,7,8]{1})([0-9]{6})([v|V|x|X]))|(([1,2]{1})([0,9]{1})([0-9]{2})([0,1,2,3,5,6,7,8]{1})([0-9]{7}))")){
                        jTextField2.setForeground(Color.BLACK);
                        
                         ResultSet resultSet =   MySQL.executeSearch(" SELECT * FROM `patient` WHERE `patient`.`nic` = '"+nic+"'");
                         
                         if(resultSet.next()){
                
                jTextField4.setText(resultSet.getString("id"));
                
                String fname = resultSet.getString("first_name");
                String lname = resultSet.getString("last_name");
                
                jLabel2.setText(fname+" " +lname);
            
            
            }else {
             jLabel2.setText("No Patient");
             jTextField4.setText("");
             reset();
            }
                         
            }
            
            
         
            
        } catch (Exception e) {
            
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextField2KeyReleased

