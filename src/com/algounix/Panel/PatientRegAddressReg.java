private void find(){

    try {
            
            String pnic = jTextField2.getText();
            String pid = jTextField4.getText();
            
            ResultSet resultSet = MySQL.executeSearch(" SELECT * FROM `patient_address` "
                    + " INNER JOIN `patient` ON `patient` . `id` = `patient_address` . `patient_id` "
                    + "INNER JOIN `district` ON `district` . `id` = `patient_address` . `district_id` "
                    + " INNER JOIN `city` ON `city` . `id` = `patient_address` . `city_id`"
                    + " WHERE `patient` . `id` = '"+pid+"' OR `patient` . `nic` = '"+pnic+"' ");
            
            if (resultSet.next()){
            
            jTextField9.setText(resultSet.getString("patient_address.id"));
            jTextField10.setText(resultSet.getString("patient_address.line1"));
            jTextField11.setText(resultSet.getString("patient_address.line2"));
            jComboBox3.setSelectedItem(resultSet.getString("city.name"));
            jComboBox4.setSelectedItem(resultSet.getString("district.name"));
            
            
            jTextField9.setEditable(false);
            
            }else {
                reset();
            
               // generateID();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
}

