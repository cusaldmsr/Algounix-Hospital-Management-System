    private void dUnit() {
        try {
            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `units` ");

            Vector<String> vector = new Vector<>();
            vector.add("SELECT");

            while (resultSet.next()) {
                vector.add(resultSet.getString("name"));
                dUnitMap.put(resultSet.getString("name"), resultSet.getString("id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void DoctorList() {

        try {

            String dUnit = String.valueOf(jComboBox1.getSelectedIndex());
            String dId = jTextField1.getText();
            String dName = jTextField1.getText();
            String dFees = jTextField1.getText();

            String query = "SELECT * FROM `doctor_has_units` "
                    + "INNER JOIN `doctor` ON `doctor_has_units`.`doctor_id`=`doctor`.`id` "
                    + "INNER JOIN `units` ON `doctor_has_units`.`units_id`=`units`.`id` "
                    + "INNER JOIN `doctor_status` ON `doctor`.`doctor_status_id`=`doctor_status`.`id` "
                    + "INNER JOIN `doctor_type` ON `doctor`.`doctor_type_id`=`doctor_type`.`id`";

            if (!dId.isEmpty()) {
                query += " WHERE `doctor`.`id` = '" + dId + "'";
            }

            if (query.contains("WHERE") && !dUnit.equals("0")) {
                query += " AND `units`.`id`='" + dUnit + "'";
            } else if (!dUnit.equals("0")) {
                query += " WHERE `units`.`id`='" + dUnit + "'";
            }

            ResultSet resultSet = MySQL.executeSearch(query);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("doctor_id"));
                vector.add(resultSet.getString("doctor.first_name"));
                vector.add(resultSet.getString("doctor_charges"));
                vector.add(resultSet.getString("units.id"));
                vector.add(resultSet.getString("units.name"));

                model.addRow(vector);
            }

        } catch (Exception e) {

            e.printStackTrace();
        }

    }

