    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String dUnit = String.valueOf(jComboBox1.getSelectedItem());
        String dID = jTextField1.getText();
        String dName = jTextField2.getText();
        String dFees = jTextField3.getText();

        if (dUnit.equals("SELECT")) {
            JOptionPane.showMessageDialog(this, "Please Select The Doctor Unit", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (dID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter The Doctor ID", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (dName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter The Doctor Name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (dFees.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter The Doctor Fees", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {
            try {
                MySQL.executeIUD("UPDATE `doctor_has_units` SET `doctor_charges` = '" + dFees + "', "
                        + " `units_id` = '" + dUnitMap.get(dUnit) + "' WHERE `doctor_id` = '" + dID + "'");

                DoctorList();
                clear();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

